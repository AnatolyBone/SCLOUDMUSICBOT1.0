    console.log(`[Referral] –ù–æ–≤—ã–π –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å ${newUser.id} –ø—Ä–∏—à–µ–ª –æ—Ç ${newUser.referrer_id}`);
    const referrerId = newUser.referrer_id;

    // 1. –ù–∞—á–∏—Å–ª—è–µ–º –±–æ–Ω—É—Å –ø—Ä–∏–≥–ª–∞—Å–∏–≤—à–µ–º—É
