async function processUrlInBackground(ctx, url) {
    let loadingMessage;
    try {
        // –û—Ç–ø—Ä–∞–≤–ª—è–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ –æ –Ω–∞—á–∞–ª–µ –∞–Ω–∞–ª–∏–∑–∞
        loadingMessage = await ctx.reply('üîç –ê–Ω–∞–ª–∏–∑–∏—Ä—É—é —Å—Å—ã–ª–∫—É...');
        const youtubeDl = getYoutubeDl();
        
        // –¢–∞ —Å–∞–º–∞—è –¥–æ–ª–≥–∞—è –æ–ø–µ—Ä–∞—Ü–∏—è. –¢–µ–ø–µ—Ä—å –æ–Ω–∞ –≤—ã–ø–æ–ª–Ω—è–µ—Ç—Å—è –∑–¥–µ—Å—å, –≤ —Ñ–æ–Ω–µ.
        const data = await youtubeDl(url, { dumpSingleJson: true, flatPlaylist: true });
        
        // --- –≠–¢–û –ü–õ–ï–ô–õ–ò–°–¢ ---
        if (data.entries && data.entries.length > 0) {
            // –£–¥–∞–ª—è–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ "–ê–Ω–∞–ª–∏–∑–∏—Ä—É—é..."
            await ctx.deleteMessage(loadingMessage.message_id).catch(() => {});
            
            const playlistId = data.id || `pl_${Date.now()}`;
            playlistSessions.set(ctx.from.id, {
                playlistId,
                title: data.title,
                tracks: data.entries,
                originalUrl: url,
                selected: new Set(),
                currentPage: 0,
                fullTracks: false
            });
            const message = `üé∂ –í –ø–ª–µ–π–ª–∏—Å—Ç–µ <b>"${data.title}"</b> –Ω–∞–π–¥–µ–Ω–æ <b>${data.entries.length}</b> —Ç—Ä–µ–∫–æ–≤.\n\n–ß—Ç–æ –¥–µ–ª–∞–µ–º?`;
            await ctx.reply(message, { parse_mode: 'HTML', ...generateInitialPlaylistMenu(playlistId, data.entries.length) });
            
            // --- –≠–¢–û –û–î–ò–ù–û–ß–ù–´–ô –¢–†–ï–ö ---
        } else {
            const user = await getUser(ctx.from.id);
            if (user.downloads_today >= user.premium_limit) {
                await ctx.telegram.editMessageText(ctx.chat.id, loadingMessage.message_id, undefined, T('limitReached'));
                return;
            }
            
            await ctx.telegram.editMessageText(ctx.chat.id, loadingMessage.message_id, undefined, '‚úÖ –†–∞—Å–ø–æ–∑–Ω–∞–ª —Ç—Ä–µ–∫, —Å—Ç–∞–≤–ª—é –≤ –æ—á–µ—Ä–µ–¥—å...');
            
            // –£–¥–∞–ª—è–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ "—Å—Ç–∞–≤–ª—é –≤ –æ—á–µ—Ä–µ–¥—å..." —á–µ—Ä–µ–∑ 3 —Å–µ–∫—É–Ω–¥—ã –¥–ª—è —á–∏—Å—Ç–æ—Ç—ã —á–∞—Ç–∞
            setTimeout(() => ctx.deleteMessage(loadingMessage.message_id).catch(() => {}), 3000);
            
            addTaskToQueue({
                userId: ctx.from.id,
                source: 'soundcloud',
                url: data.webpage_url || url,
                originalUrl: data.webpage_url || url,
                metadata: { id: data.id, title: data.title, uploader: data.uploader, duration: data.duration, thumbnail: data.thumbnail },
                ctx: null // ctx –±–æ–ª—å—à–µ –Ω–µ –ø–µ—Ä–µ–¥–∞–µ–º –≤ –æ—á–µ—Ä–µ–¥—å
            });
        }
    } catch (error) {
        console.error('–û—à–∏–±–∫–∞ –ø—Ä–∏ —Ñ–æ–Ω–æ–≤–æ–π –æ–±—Ä–∞–±–æ—Ç–∫–µ URL:', error.stderr || error.message);
        const userMessage = '‚ùå –ù–µ —É–¥–∞–ª–æ—Å—å –æ–±—Ä–∞–±–æ—Ç–∞—Ç—å —Å—Å—ã–ª–∫—É. –£–±–µ–¥–∏—Ç–µ—Å—å, —á—Ç–æ –æ–Ω–∞ –∫–æ—Ä—Ä–µ–∫—Ç–Ω–∞ –∏ –∫–æ–Ω—Ç–µ–Ω—Ç –¥–æ—Å—Ç—É–ø–µ–Ω.';
        if (loadingMessage) {
            await ctx.telegram.editMessageText(ctx.chat.id, loadingMessage.message_id, undefined, userMessage).catch(() => {});
        } else {
            await ctx.reply(userMessage);
        }
    }
}

